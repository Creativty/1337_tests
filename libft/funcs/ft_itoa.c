#	include "./libft.h"

int	ft_itoa_test(void)
{
	int	arr[] = {
		0,
		1,
		-1,
		100,
		-100,
		42069,
		100100,
		-100100,
		2147483647,
		-2147483648,
	};
	char	*exp[] = {
		"0",
		"1",
		"-1",
		"100",
		"-100",
		"42069",
		"100100",
		"-100100",
		"2147483647",
		"-2147483648",
	};
	int failures = 0;
	for (int i = 0; i < 10; i++)
	{
		char *out = ft_itoa(arr[i]);
		if (strcmp(out, exp[i]) != 0)
		{
			log_error("test %d: user: \"%s\", expected: \"%s\"", i, out, exp[i]);
			failures++;
		}
		free(out);
	}
	return (failures);
}
